    meeting_counts = Counter(qa["meeting_id"] for qa in QA_PAIRS)
    print(f"共 {len(QA_PAIRS)} 个 QA 对，覆盖 {len(meeting_counts)} 场会议\n")
    for mid, count in sorted(meeting_counts.items()):
        print(f"  {mid}: {count} 个")
    print()
    for i, qa in enumerate(QA_PAIRS, 1):
        print(f"[{i}] [{qa['meeting_id']}]")
        print(f"    Q: {qa['q']}")
        print(f"    A: {qa['a'][:60]}...")
        print()


